T = TypeVar("T", bound="PatchedAPIKeyRequest")


@_attrs_define
class PatchedAPIKeyRequest:
    """API key with usage stats.

    Attributes:
        name (Union[Unset, str]): Human-readable key name
        is_active (Union[Unset, bool]): Is key active
        expires_at (Union[None, Unset, datetime.datetime]): Key expiration
    """

    name: Union[Unset, str] = UNSET
    is_active: Union[Unset, bool] = UNSET
    expires_at: Union[None, Unset, datetime.datetime] = UNSET
    additional_properties: dict[str, Any] = _attrs_field(init=False, factory=dict)

    def to_dict(self) -> dict[str, Any]:
        name = self.name

        is_active = self.is_active

        expires_at: Union[None, Unset, str]
        if isinstance(self.expires_at, Unset):
            expires_at = UNSET
        elif isinstance(self.expires_at, datetime.datetime):
            expires_at = self.expires_at.isoformat()
        else:
            expires_at = self.expires_at

        field_dict: dict[str, Any] = {}
        field_dict.update(self.additional_properties)
        field_dict.update({})
        if name is not UNSET:
            field_dict["name"] = name
        if is_active is not UNSET:
            field_dict["is_active"] = is_active
        if expires_at is not UNSET:
            field_dict["expires_at"] = expires_at

        return field_dict

    def to_multipart(self) -> types.RequestFiles:
        files: types.RequestFiles = []

        if not isinstance(self.name, Unset):
            files.append(("name", (None, str(self.name).encode(), "text/plain")))

        if not isinstance(self.is_active, Unset):
            files.append(("is_active", (None, str(self.is_active).encode(), "text/plain")))

        if not isinstance(self.expires_at, Unset):
            if isinstance(self.expires_at, datetime.datetime):
                files.append(("expires_at", (None, self.expires_at.isoformat().encode(), "text/plain")))
            else:
                files.append(("expires_at", (None, str(self.expires_at).encode(), "text/plain")))

        for prop_name, prop in self.additional_properties.items():
            files.append((prop_name, (None, str(prop).encode(), "text/plain")))

        return files

    @classmethod
    def from_dict(cls: type[T], src_dict: Mapping[str, Any]) -> T:
        d = dict(src_dict)
        name = d.pop("name", UNSET)

        is_active = d.pop("is_active", UNSET)

        def _parse_expires_at(data: object) -> Union[None, Unset, datetime.datetime]:
            if data is None:
                return data
            if isinstance(data, Unset):
                return data
            try:
                if not isinstance(data, str):
                    raise TypeError()
                expires_at_type_0 = isoparse(data)

                return expires_at_type_0
            except:  # noqa: E722
                pass
            return cast(Union[None, Unset, datetime.datetime], data)

        expires_at = _parse_expires_at(d.pop("expires_at", UNSET))

        patched_api_key_request = cls(
            name=name,
            is_active=is_active,
            expires_at=expires_at,
        )

        patched_api_key_request.additional_properties = d
        return patched_api_key_request

