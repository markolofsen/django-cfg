"""ts_extras entry point.

Reads a (sliced) OpenAPI spec and emits zod schemas + SWR hooks +
the events bridge file into subfolders of `out_dir`. Intended to run
after Hey API has produced models + client into the same target dir.

Outputs:
    out_dir/schemas/<SchemaName>.ts
    out_dir/schemas/index.ts
    out_dir/hooks/<hookName>.ts
    out_dir/hooks/index.ts
    out_dir/events.ts
    out_dir/sdk.gen.ts          ← re-export shim → "../sdk.gen"
    out_dir/types.gen.ts        ← re-export shim → "../types.gen"

The two shim files exist so app code can import classes/types via
``@api/generated/_<group>/sdk.gen`` (group-scoped path) instead of
having to know that everything lives at the top-level ``sdk.gen`` /
``types.gen``.
"""

from __future__ import annotations

import json
from dataclasses import dataclass, field
from pathlib import Path

from .events.generator import generate_events
from .hooks.generator import generate_hooks
from .ir import build_ir, promote_inline_schemas
from .schemas.generator import generate_schemas


_SDK_SHIM = """// AUTO-GENERATED by openapi_processor / ts.tool
// Re-exports the top-level Hey API SDK so group-scoped imports work:
//   import { Foo } from "@api/generated/_<group>/sdk.gen";
// DO NOT EDIT — re-run `make gen`.
export * from "../sdk.gen";
"""

_TYPES_SHIM = """// AUTO-GENERATED by openapi_processor / ts.tool
// Re-exports the top-level Hey API types so group-scoped imports work:
//   import type { Foo } from "@api/generated/_<group>/types.gen";
// DO NOT EDIT — re-run `make gen`.
export * from "../types.gen";
"""


@dataclass(slots=True)
class TsExtrasResult:
    output_dir: Path
    files: list[Path] = field(default_factory=list)


def _write_shims(out_dir: Path) -> list[Path]:
    sdk_path = out_dir / "sdk.gen.ts"
    types_path = out_dir / "types.gen.ts"
    sdk_path.write_text(_SDK_SHIM, encoding="utf-8")
    types_path.write_text(_TYPES_SHIM, encoding="utf-8")
    return [sdk_path, types_path]


def generate(
    spec_path: Path,
    out_dir: Path,
    *,
    extras: list[str],
    enum_prefix: str = "",
) -> TsExtrasResult:
    """Generate the requested extras into `out_dir`.

    `extras` selects which sub-generators run: {"zod", "hooks", "events"}.
    Order is fixed: zod → hooks → events (events is purely additive, no
    cross-deps).

    `enum_prefix` — when provided (typically the group/tag name, e.g.
    ``"crm"``), all hash-suffixed enum names produced by drf-spectacular
    collision resolution (e.g. ``StatusA3aEnum``) are renamed to
    ``<PascalPrefix><BaseName>Enum`` (e.g. ``CrmStatusEnum``) before any
    files are written. This makes enum names stable across regenerations
    and avoids cryptic hash suffixes in generated TypeScript.
    """
    spec = json.loads(spec_path.read_text(encoding="utf-8"))
    promote_inline_schemas(spec)
    ir = build_ir(spec)
    out_dir.mkdir(parents=True, exist_ok=True)

    files: list[Path] = []
    if "zod" in extras:
        files.extend(generate_schemas(ir, out_dir / "schemas", enum_prefix=enum_prefix))
    if "hooks" in extras:
        files.extend(generate_hooks(ir, out_dir / "hooks"))
    if "events" in extras:
        files.extend(generate_events(out_dir))
    files.extend(_write_shims(out_dir))
    return TsExtrasResult(output_dir=out_dir, files=files)
