"""Render a SWR mutation hook for a non-GET operation.

The mutation accepts a single ``arg`` matching Hey API's ``Data`` type
shape — ``{ path?, query?, body? }`` — which is forwarded to the SDK
verbatim. Path params are part of the SWR key when present, so callers
that mount the hook at a fixed resource still get scoped revalidation.
"""

from __future__ import annotations

from ._validation import runtime_validation_block
from .sdk_names import sdk_class_name, sdk_fn_name, sdk_type_names
from ..ir import IROperation


def render_mutation(op: IROperation, hook: str) -> str:
    fn_name = sdk_fn_name(op)
    cls_name = sdk_class_name(op)
    data_t, resp_t = sdk_type_names(op)
    summary = op.summary or f"{op.method.upper()} {op.path}"
    validation = runtime_validation_block(op)

    return f"""'use client';

// AUTO-GENERATED by openapi_processor / ts.hooks
// {summary}
// DO NOT EDIT — re-run `make gen`.

import useSWRMutation from "swr/mutation";
import type {{ SWRMutationConfiguration }} from "swr/mutation";
import {{ {cls_name} }} from "../../sdk.gen";
import type {{ {data_t}, {resp_t} }} from "../../types.gen";
{validation.imports}

type Args = Omit<{data_t}, "url">;
type Result = {resp_t}[keyof {resp_t}];

export function {hook}(
  config?: SWRMutationConfiguration<Result, Error, readonly ["{op.operation_id}"], Args>,
) {{
  return useSWRMutation<Result, Error, readonly ["{op.operation_id}"], Args>(
    ["{op.operation_id}"] as const,
    async (_key, {{ arg }}: {{ arg: Args }}) => {{
      const res = await {cls_name}.{fn_name}({{ ...arg, throwOnError: true }} as never);
      const data = res.data as Result;
{validation.body}
    }},
    config,
  );
}}
"""
