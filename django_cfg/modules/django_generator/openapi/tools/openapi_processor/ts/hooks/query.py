"""Render a SWR query hook for a GET operation.

The hook signature mirrors Hey API's ``Data`` type — args are passed as a
single object with ``{ path?, query? }`` slots and forwarded to the SDK
verbatim. This matches how the underlying SDK function expects its options
and avoids the app having to flatten/unwrap params.
"""

from __future__ import annotations

from ._validation import runtime_validation_block
from .sdk_names import sdk_class_name, sdk_fn_name, sdk_type_names
from ..ir import IROperation


def _has_required_args(op: IROperation) -> bool:
    """True when the SDK requires args (any path param, or a required query param)."""
    if op.path_params:
        return True
    return any(p.required for p in op.query_params)


def render_query(op: IROperation, hook: str) -> str:
    fn_name = sdk_fn_name(op)
    cls_name = sdk_class_name(op)
    data_t, resp_t = sdk_type_names(op)
    summary = op.summary or f"{op.method.upper()} {op.path}"

    required = _has_required_args(op)

    # Args type mirrors Hey API's Data type — { body?, path?, query? }.
    # We omit `url` (SDK adds it) and `body` (GET hooks don't send a body).
    args_type = f'Omit<{data_t}, "url" | "body">'

    if required:
        args_sig = f"args: {args_type},"
        key_expr = f'["{op.operation_id}", args] as const'
        sdk_arg = "args"
    else:
        args_sig = f"args?: {args_type},"
        key_expr = f'["{op.operation_id}", args ?? {{}}] as const'
        sdk_arg = "(args ?? {})"

    validation = runtime_validation_block(op)

    return f"""'use client';

// AUTO-GENERATED by openapi_processor / ts.hooks
// {summary}
// DO NOT EDIT — re-run `make gen`.

import useSWR from "swr";
import type {{ SWRConfiguration }} from "swr";
import {{ {cls_name} }} from "../../sdk.gen";
import type {{ {data_t}, {resp_t} }} from "../../types.gen";
{validation.imports}

type Result = {resp_t}[keyof {resp_t}];

export function {hook}(
  {args_sig}
  config?: SWRConfiguration<Result>,
) {{
  const key = {key_expr};
  return useSWR<Result>(
    key,
    async () => {{
      const res = await {cls_name}.{fn_name}({{ ...{sdk_arg}, throwOnError: true }} as never);
      const data = res.data as Result;
{validation.body}
    }},
    config,
  );
}}
"""
