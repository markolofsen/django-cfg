"""Render a useSWRInfinite hook for paginated list operations.

Generated **in addition** to the regular ``useSWR`` page-based hook for
paginated endpoints (response schema ref starts with ``Paginated``).
The hook name has the ``Infinite`` suffix:

    useApiKeysList          ← page-based, returns one page at a time
    useApiKeysListInfinite  ← infinite scroll, returns flatData

Default usage in app code is the page-based hook. Reach for the
infinite variant only for genuine infinite-scroll surfaces (feeds,
chats), not for tables with page numbers.

The generated hook:
 - Uses ``useSWRInfinite`` from swr/infinite
 - Accepts ``{ path?, query? }`` args matching the Hey API Data type
 - Strips ``page`` from the query type (the hook manages it internally)
 - Injects ``page`` automatically via ``getKey``
 - Exposes ``flatData`` — all ``results`` arrays concatenated
 - Returns ``count``, ``has_next``, ``has_previous`` from the last page
"""

from __future__ import annotations

from ._validation import runtime_validation_block
from .sdk_names import sdk_class_name, sdk_fn_name, sdk_type_names
from ..ir import IROperation


def _has_required_args(op: IROperation) -> bool:
    """Args are required when the SDK requires path params or non-paging required query."""
    if op.path_params:
        return True
    return any(p.required for p in op.query_params if p.name != "page")


def render_infinite_query(op: IROperation, hook: str) -> str:
    fn_name = sdk_fn_name(op)
    cls_name = sdk_class_name(op)
    data_t, resp_t = sdk_type_names(op)
    summary = op.summary or f"{op.method.upper()} {op.path}"
    # Same validation block as the page-based hook — each fetched
    # page goes through the same schema, so we validate per-page
    # instead of validating the whole concatenated stream.
    validation = runtime_validation_block(op)

    required = _has_required_args(op)

    page_result = f"{resp_t}[keyof {resp_t}]"

    args_type = (
        f'Omit<{data_t}, "url" | "body"> & '
        f'{{ query?: Omit<NonNullable<{data_t}["query"]>, "page"> }}'
    )

    if required:
        args_sig = f"args: {args_type},"
        key_fn = (
            f"(pageIndex: number) => "
            f'["{op.operation_id}_infinite", args, pageIndex + 1] as const'
        )
        sdk_args_spread = "...args, query: { ...(args.query ?? {}), page }"
    else:
        args_sig = f"args?: {args_type},"
        key_fn = (
            f"(pageIndex: number) => args !== undefined\n"
            f'    ? ["{op.operation_id}_infinite", args, pageIndex + 1] as const\n'
            f"    : null"
        )
        sdk_args_spread = "...(args ?? {}), query: { ...(args?.query ?? {}), page }"

    return f"""'use client';

// AUTO-GENERATED by openapi_processor / ts.hooks
// {summary} (infinite scroll variant)
// DO NOT EDIT — re-run `make gen`.

import useSWRInfinite from "swr/infinite";
import type {{ SWRInfiniteConfiguration }} from "swr/infinite";
import {{ {cls_name} }} from "../../sdk.gen";
import type {{ {data_t}, {resp_t} }} from "../../types.gen";
{validation.imports}

type PageResult = {page_result};
type Result = PageResult;

export function {hook}(
  {args_sig}
  config?: SWRInfiniteConfiguration<PageResult>,
) {{
  const getKey = {key_fn};

  const swr = useSWRInfinite<PageResult>(
    getKey,
    async (key) => {{
      const page = key[key.length - 1] as number;
      const res = await {cls_name}.{fn_name}({{ {sdk_args_spread}, throwOnError: true }} as never);
      const data = res.data as Result;
{validation.body}
    }},
    config,
  );

  const pages = swr.data ?? [];
  const last = pages[pages.length - 1] as (PageResult & {{ has_next?: boolean; has_previous?: boolean; count?: number }}) | undefined;

  return {{
    ...swr,
    flatData: pages.flatMap((p) => ((p as {{ results?: unknown[] }}).results ?? [])),
    count: last?.count ?? 0,
    has_next: last?.has_next ?? false,
    has_previous: last?.has_previous ?? false,
    loadMore: () => swr.setSize((s) => s + 1),
  }};
}}
"""
