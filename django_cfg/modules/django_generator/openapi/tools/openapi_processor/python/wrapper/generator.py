"""Generator for one ``<Tag>API`` class per tag-group.

Walks ``out_dir/<tag>/api/<tag>/*.py`` (the layout produced by
``openapi-python-client``), parses each operation file's
``async def asyncio(...)`` signature with the stdlib ``ast`` module,
and emits ``out_dir/<tag>/api.py`` with a thin convenience class.

No Jinja: pure string templating to keep the dependency surface zero
(stdlib only — matches the rest of ``openapi_processor``).
"""

from __future__ import annotations

import ast
import re
from dataclasses import dataclass, field
from pathlib import Path


# ── Models ──────────────────────────────────────────────────────────


@dataclass(slots=True)
class Param:
    name: str
    annotation: str | None
    default: str | None  # source-text default (None if required)
    is_keyword_only: bool


@dataclass(slots=True)
class Operation:
    """One operation function inside ``api/<tag>/<file>.py``.

    ``module`` is the file stem (the operation function name) — used
    both as Python module name and method name on the wrapper class.
    """

    module: str
    params: list[Param]
    return_annotation: str | None


@dataclass(slots=True)
class WrapperResult:
    out_dir: Path
    written: list[Path] = field(default_factory=list)


# ── AST extraction ──────────────────────────────────────────────────


def _unparse(node: ast.AST | None) -> str | None:
    if node is None:
        return None
    return ast.unparse(node)


def extract_operation(source: str, module_name: str) -> Operation | None:
    """Parse ``async def asyncio(...)`` from one operation file.

    Returns ``None`` if the file has no ``asyncio`` coroutine — that
    happens for ``__init__.py`` and any helper module. The wrapper
    only mounts real operations.
    """
    tree = ast.parse(source)
    for stmt in tree.body:
        if isinstance(stmt, ast.AsyncFunctionDef) and stmt.name == "asyncio":
            return _build_operation(stmt, module_name)
    return None


def _build_operation(fn: ast.AsyncFunctionDef, module_name: str) -> Operation:
    args = fn.args
    params: list[Param] = []

    # Positional + positional-or-keyword
    pos_args = list(args.posonlyargs) + list(args.args)
    pos_defaults = list(args.defaults)
    # Defaults align to the tail of ``pos_args``.
    pad = [None] * (len(pos_args) - len(pos_defaults))
    pos_default_nodes: list[ast.expr | None] = pad + pos_defaults  # type: ignore[assignment]

    for arg, default in zip(pos_args, pos_default_nodes):
        if arg.arg == "client":
            continue  # the wrapper supplies ``client``
        params.append(
            Param(
                name=arg.arg,
                annotation=_unparse(arg.annotation),
                default=_unparse(default),
                is_keyword_only=False,
            )
        )

    # Keyword-only
    for arg, default in zip(args.kwonlyargs, args.kw_defaults):
        if arg.arg == "client":
            continue
        params.append(
            Param(
                name=arg.arg,
                annotation=_unparse(arg.annotation),
                default=_unparse(default),
                is_keyword_only=True,
            )
        )

    return Operation(
        module=module_name,
        params=params,
        return_annotation=_unparse(fn.returns),
    )


# ── Rendering ───────────────────────────────────────────────────────


_HEADER = '''"""Auto-generated API wrapper — DO NOT EDIT.

Regenerated by openapi_processor.python.wrapper on every ``make gen``.
"""

from __future__ import annotations

import os
from typing import TYPE_CHECKING, Any

from .client import AuthenticatedClient, Client
from .types import UNSET, Unset

if TYPE_CHECKING:
    # Models are referenced only in stringified annotations (PEP 563).
    # Import them lazily for type checkers without paying at runtime.
    from .models import *  # noqa: F401,F403
'''


def _camel_class_name(tag: str) -> str:
    """``catalog_public`` → ``CatalogPublic``; ``operations`` → ``Operations``."""
    return "".join(p[:1].upper() + p[1:] for p in re.split(r"[_\-]", tag) if p)


def _strip_tag_prefix(method: str, tag: str) -> str:
    """Drop ``<tag>_`` from operation file name when present.

    ``operations_sync_active_retrieve`` + tag ``operations``
        → ``sync_active_retrieve``
    """
    prefix = f"{tag}_"
    return method[len(prefix):] if method.startswith(prefix) else method


def _format_param(p: Param) -> str:
    chunk = p.name
    if p.annotation:
        chunk += f": {p.annotation}"
    if p.default is not None:
        chunk += f" = {p.default}"
    return chunk


def render_method(op: Operation, tag: str) -> str:
    """Render one ``async def`` method of ``<Tag>API``."""
    method_name = _strip_tag_prefix(op.module, tag)

    # Build signature: positional args first, then ``*`` then kw-only.
    pos_chunks = [_format_param(p) for p in op.params if not p.is_keyword_only]
    kw_chunks = [_format_param(p) for p in op.params if p.is_keyword_only]

    sig_parts = ["self"]
    sig_parts.extend(pos_chunks)
    if kw_chunks:
        sig_parts.append("*")
        sig_parts.extend(kw_chunks)
    sig = ", ".join(sig_parts)

    ret = f" -> {op.return_annotation}" if op.return_annotation else ""

    # Call: each param forwarded by name. ``client`` is supplied here.
    call_args = ["client=self._client"]
    call_args.extend(f"{p.name}={p.name}" for p in op.params)
    call_str = ", ".join(call_args)

    return (
        f"    async def {method_name}({sig}){ret}:\n"
        f"        from .api.{tag} import {op.module}\n"
        f"        return await {op.module}.asyncio({call_str})\n"
    )


def render_class(tag: str, operations: list[Operation]) -> str:
    """Render the full ``<Tag>API`` module text."""
    class_name = f"{_camel_class_name(tag)}API"
    methods = "\n".join(render_method(op, tag) for op in operations)
    if not methods:
        # No-ops group: still emit the class so the barrel resolves.
        methods = "    pass\n"

    body = f'''class {class_name}:
    """Convenience wrapper around the {tag!r} tag operations.

    Auth is selected at construction time:

    * ``token``    → ``Authorization: Bearer <token>`` via ``AuthenticatedClient``
    * ``api_key``  → ``x-api-key`` header via ``Client(httpx_args=...)``
    * neither     → unauthenticated ``Client``

    Use as an async context manager or directly::

        async with {class_name}(api_key="...") as api:
            result = await api.<method>(...)
    """

    DEFAULT_TIMEOUT = 60.0
    BASE_URL_ENV = "API_BASE_URL"

    def __init__(
        self,
        base_url: str | None = None,
        *,
        token: str | None = None,
        api_key: str | None = None,
        timeout: float | None = None,
        **kwargs: Any,
    ) -> None:
        # Resolve base_url: explicit arg > env var > error.
        # Env var name is overridable per subclass via BASE_URL_ENV.
        resolved_base_url = base_url or os.getenv(self.BASE_URL_ENV)
        if not resolved_base_url:
            raise ValueError(
                "base_url is required: pass it explicitly or set "
                + self.BASE_URL_ENV
            )
        base_url = resolved_base_url
        client_timeout = timeout if timeout is not None else self.DEFAULT_TIMEOUT
        if token:
            self._client: AuthenticatedClient | Client = AuthenticatedClient(
                base_url=base_url,
                token=token,
                timeout=client_timeout,
                **kwargs,
            )
        elif api_key:
            self._client = Client(
                base_url=base_url,
                timeout=client_timeout,
                httpx_args={{"headers": {{"x-api-key": api_key}}}},
                **kwargs,
            )
        else:
            self._client = Client(base_url=base_url, timeout=client_timeout, **kwargs)

    def set_token(self, token: str) -> None:
        """Replace auth with Bearer ``token`` for subsequent calls."""
        base_url = self._client._base_url  # noqa: SLF001 — generated client API
        timeout = getattr(self._client, "_timeout", self.DEFAULT_TIMEOUT)
        self._client = AuthenticatedClient(base_url=base_url, token=token, timeout=timeout)

    def set_api_key(self, api_key: str) -> None:
        """Replace auth with ``x-api-key`` header for subsequent calls."""
        base_url = self._client._base_url  # noqa: SLF001
        timeout = getattr(self._client, "_timeout", self.DEFAULT_TIMEOUT)
        self._client = Client(
            base_url=base_url,
            timeout=timeout,
            httpx_args={{"headers": {{"x-api-key": api_key}}}},
        )

    async def __aenter__(self) -> "{class_name}":
        return self

    async def __aexit__(self, *exc: Any) -> None:
        return None

{methods}'''
    return _HEADER + "\n\n" + body


# ── Top-level entry point ───────────────────────────────────────────


def discover_operations(group_dir: Path) -> tuple[str, list[Operation]] | None:
    """Find the tag dir and parse all operation files inside it.

    ``group_dir`` is e.g. ``out_dir/operations``. Inside it, the layout
    written by openapi-python-client is::

        client.py  errors.py  models/  types.py
        api/<tag>/<operation>.py

    Returns ``(tag, [Operation, ...])`` or ``None`` when there is no
    ``api/`` subdir (means the group is empty).
    """
    api_root = group_dir / "api"
    if not api_root.is_dir():
        return None
    tag_dirs = [p for p in api_root.iterdir() if p.is_dir() and not p.name.startswith("_")]
    if not tag_dirs:
        return None
    # openapi-python-client emits exactly one tag-folder per group spec
    # (we slice per-group upstream). Defensive: take the lexicographic
    # first if multiple appear.
    tag_dir = sorted(tag_dirs, key=lambda p: p.name)[0]
    tag = tag_dir.name
    ops: list[Operation] = []
    for py in sorted(tag_dir.glob("*.py")):
        if py.name == "__init__.py":
            continue
        op = extract_operation(py.read_text(encoding="utf-8"), py.stem)
        if op is not None:
            ops.append(op)
    return tag, ops


def generate(out_dir: Path) -> WrapperResult:
    """Emit ``wrapper.py`` next to each python-client output.

    Two layouts are supported:

    1. ``out_dir`` is itself a python-client output (has ``client.py``)
       — the typical per-group target after slicing by the runner.
       Writes one ``out_dir/wrapper.py``.
    2. ``out_dir`` is a parent containing multiple group subdirs each
       with its own ``client.py``. Writes a ``wrapper.py`` per subdir.

    Note: file is named ``wrapper.py`` (not ``api.py``) because
    python-client already produces an ``api/`` package next to it,
    and the package would shadow a same-name module on import.
    """
    written: list[Path] = []
    if (out_dir / "client.py").is_file():
        candidates = [out_dir]
    else:
        candidates = [
            p for p in sorted(out_dir.iterdir())
            if p.is_dir() and (p / "client.py").is_file()
        ]
    for group_dir in candidates:
        discovered = discover_operations(group_dir)
        if discovered is None:
            continue
        tag, operations = discovered
        text = render_class(tag, operations)
        target = group_dir / "wrapper.py"
        target.write_text(text, encoding="utf-8")
        written.append(target)
    return WrapperResult(out_dir=out_dir, written=written)


def generate_barrel(parent_dir: Path) -> Path | None:
    """Emit ``parent_dir/__init__.py`` re-exporting every group's API class.

    Walks every immediate subdir of ``parent_dir`` that has a generated
    ``wrapper.py`` and emits one ``from .<group>.wrapper import <Group>API``
    line per group. Drops a final ``__all__`` listing all exported names.

    Returns the path written, or ``None`` if no wrappers were found.
    """
    if not parent_dir.is_dir():
        return None
    entries: list[tuple[str, str]] = []
    for sub in sorted(parent_dir.iterdir()):
        if not sub.is_dir():
            continue
        if not (sub / "wrapper.py").is_file():
            continue
        # Tag (and thus class name) is whatever the wrapper.py declares.
        # We can't always rely on subdir name == tag (group name may
        # differ from the OpenAPI tag), so re-parse the wrapper file.
        text = (sub / "wrapper.py").read_text(encoding="utf-8")
        match = re.search(r"^class\s+(\w+)\s*:", text, re.MULTILINE)
        if not match:
            continue
        entries.append((sub.name, match.group(1)))

    if not entries:
        return None

    lines = [
        '"""Auto-generated API barrel — DO NOT EDIT.',
        "",
        "Re-exports every <Group>API class from per-group wrapper modules.",
        "Regenerated by openapi_processor.python.wrapper on every ``make gen``.",
        '"""',
        "",
        "from __future__ import annotations",
        "",
    ]
    for group, cls in entries:
        lines.append(f"from .{group}.wrapper import {cls}")
    lines.append("")
    names = ", ".join(f'"{cls}"' for _, cls in entries)
    lines.append(f"__all__ = [{names}]")
    lines.append("")

    target = parent_dir / "__init__.py"
    target.write_text("\n".join(lines), encoding="utf-8")
    return target


