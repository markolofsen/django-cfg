def build_h3_proxy_from_server_config(
    server_cfg: "GrpcServerConfig",
) -> H3Proxy:
    """
    Build an H3Proxy from a GrpcServerConfig.

    The upstream is always the gRPC server's host:port — when the gRPC server
    binds a wildcard address ([::], 0.0.0.0) we connect to loopback, since the
    H3 process and the gRPC process are the same Python interpreter.

    TLS certs are pulled from `server_cfg.tls` when configured. Without certs,
    Hypercorn serves cleartext (no real H3) — see H3Proxy._build_hypercorn_config.
    """
    tls_certfile: Optional[str] = None
    tls_keyfile: Optional[str] = None
    tls_cfg = getattr(server_cfg, "tls", None)
    if tls_cfg is not None and getattr(tls_cfg, "enabled", False):
        tls_certfile = getattr(tls_cfg, "cert_path", None)
        tls_keyfile = getattr(tls_cfg, "key_path", None)

    return H3Proxy(
        upstream_host=server_cfg.host,
        upstream_port=server_cfg.port,
        listen_host=server_cfg.h3_host,
        listen_port=server_cfg.h3_port,
        tls_certfile=tls_certfile,
        tls_keyfile=tls_keyfile,
    )


__all__ = ["H3Proxy", "build_h3_proxy_from_server_config"]
